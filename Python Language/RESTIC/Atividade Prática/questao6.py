# Questão 6 - Quais são os operadores comparativos usados em Python? Dê exemplos.
# Igual a (==)
print(5 == 5)    # True
print('abc' == 'def')  # False

# Diferente de (!=)
print(5 != 3)    # True
print('python' != 'Python')  # True

# Maior que (>)
print(10 > 5)    # True
print('b' > 'a')    # True

# Menor que (<)
print(3 < 7)    # True
print('apple' < 'banana')  # True

# Maior ou igual a (>=)
print(5 >= 5)    # True
print(8 >= 10)    # False

# Menor ou igual a (<=)
print(3 <= 6)    # True
print('dog' <= 'cat')  # False
